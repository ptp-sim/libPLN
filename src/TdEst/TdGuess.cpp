TdGuess::TdGuess()
    : fp( 0.0, 0.0 )
{
    active   = false;
}

bool
TdGuess::IsActive()
{
    return active;
}

bool
TdGuess::BecameValid( double t_now )
{
    return (fp.Get_t() <= t_now);
}

void
TdGuess::Set( TdFixPoint fp )
{
    assert( active == false );

    this->fp        = fp;
    this->active    = true;
}

TdFixPoint
TdGuess::GetFP()
{
    assert( active == true );

    return  fp;
}

void
TdGuess::Reset()
{
    assert( active == true );

    active   = false;
}
