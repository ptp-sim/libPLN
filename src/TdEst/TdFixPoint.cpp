TdFixPoint::TdFixPoint( double t, double TD_nom )
{
    this->t         = t;
    this->TD_nom    = TD_nom;
}

double
TdFixPoint::Get_t() const
{
    return t;
}

double
TdFixPoint::GetTD_nom() const
{
    return TD_nom;
}

bool
TdFixPoint::operator<( const TdFixPoint& other ) const
{
    return this->t < other.t;
}
