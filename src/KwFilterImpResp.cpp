KwFilterImpResp::KwFilterImpResp( size_t FilterLen, double alpha )
    : FilterImpResp( FilterLen )
{
    double ha = -alpha/2.0;

    h_[0]= 1.0;

    for( size_t i=1; i < FilterLen ; i++ )
    {
        // generate the coefficients h_k
        h_[i] = h_[i-1]* (ha + (double)(i-1))/((double)(i));
    }
}
